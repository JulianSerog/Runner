import UIKit

extension UIColor
{
    class func color1() ->UIColor
    {
        return UIColor(red: 0.255, green: 0.33, blue: 0.33, alpha: 1)
    }//color1
    
    class func color2() ->UIColor
    {
        return UIColor(red: 0.4, green: 0.7, blue: 0.2, alpha: 1)
    }//color 2
}//extension